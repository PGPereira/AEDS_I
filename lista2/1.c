#include<stdio.h>

int main(){
        int a, b;

        scanf ("%d %d", &a, &b);

        printf ("soma: %d\n", a + b);
        printf ("subtração: %d\n", a - b);
        printf ("multiplicação: %d\n", a * b);
        printf ("divisão: %d\n", a / b);
        printf ("modulo: %d\n", a % b);

        return 0;
}
